						<ul>
							<li>A prospective tenants applying for a home in a USDA property must have the Form 3560-8 filled out for them by the Property Manager.</li>
							<li>The applicant(s) and the manager do NOT sign the form before it is turned into the Compliance Manager.</li>
							<li>The Compliance Manager will approve or deny the form and send it back to the Property Manager.</li>
							<li>If the applicant is approved and the property is a part of the 515 program, they will sign the form and send it to the MINC specialist.</li>
							<li>The NAHASDA program does NOT submit the form to the MINC specialist</li>
