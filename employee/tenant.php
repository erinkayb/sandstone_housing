						<h4>Recertification</h4>
						<ul>
							<li>Send ALL the USDA 515, HUD Section 8 and Tax Credit section 42 recertification notices by:
								<ol>
									<li>Certified mail</li>
									<li>Regular mail</li>
									<li>Post on door</li>
									<li>Place copy of postings in resident file</li>
									<li>Scan and email a copy of the certified mail receipts from post office along with the notice to the OneSite Specialist and the Compliance Managers.</li>
									<li>Email copies of pro-active documentation efforts through-out the process to OneSite Specialist and the Compliance Managers, and insert in the resident file.</li>
								</ol>
							</li>
							<li>This is a required procedure and will remain in effect for all 120, 90, 60, and 30 day notices. Utilize your MINC Work Sheets, Rent Roll Detail, and Lease Expiration Reports from OneSite to help you with this process.</li>
							<li>We must eliminate any last day rushes. This can result in penalties and fines against Sandstone Housing. We must adhere to all agency requirements and eliminate any further penalties, fines, and non-compliance findings to ensure Sandstone Housing performs financially.</li>
						</ul>
						
