						<p>To download copies of the forms to your computer, click on the title of the form (on the right).</p>
						<h4>Sandstone Housing Prior to Move-in Procedures</h4>
						<ol>
							<li>Manager/Assistant Manager will inform Lead Maintenance staff when a unit has been leased to prospective tenant.</li>
							<li>Manager/Assistant Manager will inform Lead Maintenance staff when that prospective tenant’s initial screening has been approved or denied.</li>
							<li>Manager/Assistant Manager will inform Lead Maintenance staff of tentative move-in date once applicant has passed initial screening. If unit is not ready, Lead Maintenance staff should submit their supply order to Manager/Assistant Manager at this time. Manager/Assistant Manager must immediately submit order to ensure supplies are in stock for market ready. All market readies must be completed within five days.</li>
							<li>Manager/Assistant Manager will meet with Lead Maintenance staff each and every Friday to discuss progress of market ready units and/or leased units.</li>
							<li>Lead Maintenance staff will inform Manager/Assistant Manager as soon as a vacant or leased unit is deemed market ready.</li>
							<li>Lead Maintenance staff and Manager/Assistant will walk the market ready unit together and both parties must agree that vacant or leased unit is in fact market ready or make necessary corrections.</li>
							<li>If corrections are needed then it will be necessary for both parties to walk the unit again until corrections have been satisfied by both parties and unit is in fact market ready. Only then will the Vacant Unit Status Sheet reflect unit is complete.</li>
							<li>Manager/Assistant Manager will inform Lead Maintenance staff of any changes in move-in dates or denied applicants.</li>
							<li>Manager/Assistant Manager will walk Leased unit two days prior to move-in. Standing units may be damaged (broken window, etc.) without anyone’s knowledge.</li>
							<li>Manager/Assistant Manager MUST walk unit with new resident prior to signing the lease to assure new resident will accept the unit as decent, safe and sanitary. Move-in checklist MUST be signed by new resident and Manager/Assistant Manager. If need be, a work order will be generated to address any deficiencies but the move-in checklist must never leave the new resident file. Move-in process may commence.</li>
						</ol>
						
